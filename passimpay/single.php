<section>
<div class="container">


			<?php
			// Start the Loop.
			while ( have_posts() ) :
				the_post();

				get_template_part( 'template-parts/post/content', get_post_format() );

				// If comments are open or we have at least one comment, load up the comment template.
							

			endwhile; // End the loop.
			?>

	
	</div><!-- #primary -->
	
